class p12_PrintVsPrintln
{
    public static void main(String[] args)
    {
        System.out.println("*");
        System.out.println("*");
        System.out.println("*");
        
        // Now, the cursor will not move to next line
        System.out.print("*");
        System.out.print("*");
        System.out.print("*");
    }
}
class p1_ArithmeticOperatorsInJava
{
    public static void main(String[] args)
    {
        // Arithmetic Operators :-
        int n1 = 10;
        int n2 = 3;
        System.out.println(n1+n2);
        System.out.println(n1-n2);
        System.out.println(n1*n2);
        System.out.println(n1/n2); //this will result 3, 0.33... will be truncated, int-int wil give int.
        System.out.println(n1%n2);
    }
}
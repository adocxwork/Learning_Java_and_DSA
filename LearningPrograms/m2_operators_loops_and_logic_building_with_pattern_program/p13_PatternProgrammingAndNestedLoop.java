class p13_PatternProgrammingAndNestedLoop
{
    public static void main(String[] args)
    {
        int n = 7;
        for(int j=0; j<n; j++)
        {
            for(int i=0; i<n; i++)
            {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
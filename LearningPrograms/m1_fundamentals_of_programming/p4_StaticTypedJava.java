class p4_StaticTypedJava {
    public static void main(String[] args) {
        
        int a = 10;
        // Type VariableName = Value/Literal;

        // Java is a statically typed programming language where,
        // we need to define the type of data which is to be assigned to a variable at the time of its declaration,
        // before the compilation of program

        // Memory for variables is provided at the time of compilation
        // int a; >> declaration
        // int a = 1; >> initialization i.e., assigning an initial value.
        System.out.println(a);
    }
    
}
